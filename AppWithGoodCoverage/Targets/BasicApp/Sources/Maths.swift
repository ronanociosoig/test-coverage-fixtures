import Foundation

struct Maths {
    static func add(_ a: Int, _ b: Int) -> Int {
        return a + b
    }
    
    static func subtract(_ a: Int, _ b: Int) -> Int {
        return a - b
    }
    
    static func multiply(_ a: Int, _ b: Int) -> Int {
        return a * b
    }
    
    static func divide(_ a: Int, _ b: Int) -> Int {
        return a / b
    }
}
